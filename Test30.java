//给定一个排序数组和一个目标值，在数组中找到目标值，并返回其索引。如果目标值不存在于数组中，返回它将会被按顺序插入的位置。

//你可以假设数组中无重复元素。
class Solution {
    public int searchInsert(int[] nums, int target) {
        int len = nums.length;
        for(int i = 0;i <len;i++) {
            if(nums[i] > target) {
                return 0;
            }
            if(nums[i]==target) {
                return i;
            }
            if(nums[i] < target  && i+1 <len&& nums[i+1] > target) {
                return i+1;
            }
           
        }
        return len;
    }
}